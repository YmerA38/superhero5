package com.example.superhero5.entity;

public class Power {
    private String name;
    private int powerId;

    public Power() {
    }

    public String getName() {
        return name;
    }

    public int getPowerId() {
        return powerId;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPowerId(int powerId) {
        this.powerId = powerId;
    }
}
